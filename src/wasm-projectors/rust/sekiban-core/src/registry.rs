use crate::command::CommandMeta;
use crate::event::EventPayload;
use crate::projector::{
    MultiProjector, MultiProjectorQuery, ProjectorInfo, ProjectorKind, TagProjector,
};
use crate::query::{ListQuery, Query};
use crate::state::StatePayload;
use crate::tag::Tag;

/// Domain type registry.
#[derive(Debug, Clone)]
pub struct DomainTypes {
    pub events: Vec<EventInfo>,
    pub tags: Vec<TagInfo>,
    pub tag_projectors: Vec<ProjectorInfo>,
    pub multi_projectors: Vec<ProjectorInfo>,
    pub commands: Vec<CommandInfo>,
    pub queries: Vec<QueryInfo>,
}

/// Event metadata.
#[derive(Debug, Clone)]
pub struct EventInfo {
    pub event_type: &'static str,
    pub deserializer: fn(&str) -> Option<Box<dyn std::any::Any + Send + Sync>>,
}

/// Tag metadata.
#[derive(Debug, Clone)]
pub struct TagInfo {
    pub group: &'static str,
}

/// Command metadata.
#[derive(Debug, Clone)]
pub struct CommandInfo {
    pub command_type: &'static str,
}

/// Query metadata.
#[derive(Debug, Clone)]
pub struct QueryInfo {
    pub query_type: &'static str,
    pub is_list: bool,
}

/// Domain registry builder.
pub struct DomainTypesBuilder {
    events: Vec<EventInfo>,
    tags: Vec<TagInfo>,
    tag_projectors: Vec<ProjectorInfo>,
    multi_projectors: Vec<ProjectorInfo>,
    commands: Vec<CommandInfo>,
    queries: Vec<QueryInfo>,
}

/// Projector registrar for external registries (e.g. WASM factories).
pub trait ProjectorRegistrar {
    fn register_tag_projector<P: TagProjector>(&mut self);
    fn register_multi_projector<P: MultiProjector + MultiProjectorQuery>(&mut self);
}

/// Domain projector registration (generated by domain_types! macro).
pub trait DomainProjectorRegistration {
    fn register_projectors<R: ProjectorRegistrar>(registrar: &mut R);
}

impl DomainTypesBuilder {
    pub fn new() -> Self {
        Self {
            events: Vec::new(),
            tags: Vec::new(),
            tag_projectors: Vec::new(),
            multi_projectors: Vec::new(),
            commands: Vec::new(),
            queries: Vec::new(),
        }
    }

    pub fn register_event<E: EventPayload>(&mut self) -> &mut Self {
        self.events.push(EventInfo {
            event_type: E::EVENT_TYPE,
            deserializer: |json| {
                serde_json::from_str::<E>(json)
                    .ok()
                    .map(|e| Box::new(e) as Box<dyn std::any::Any + Send + Sync>)
            },
        });
        self
    }

    pub fn register_tag<T: Tag>(&mut self) -> &mut Self {
        self.tags.push(TagInfo {
            group: T::TAG_GROUP,
        });
        self
    }

    pub fn register_tag_projector<P: TagProjector>(&mut self) -> &mut Self {
        self.tag_projectors.push(ProjectorInfo {
            name: P::PROJECTOR_NAME,
            version: P::PROJECTOR_VERSION,
            kind: ProjectorKind::Tag,
            state_type: P::State::STATE_TYPE,
            event_types: P::event_types(),
        });
        self
    }

    pub fn register_multi_projector<P: MultiProjector>(&mut self) -> &mut Self {
        self.multi_projectors.push(ProjectorInfo {
            name: P::PROJECTOR_NAME,
            version: P::PROJECTOR_VERSION,
            kind: ProjectorKind::Multi,
            state_type: P::State::STATE_TYPE,
            event_types: P::event_types(),
        });
        self
    }

    pub fn register_command<C: CommandMeta>(&mut self) -> &mut Self {
        self.commands.push(CommandInfo {
            command_type: C::COMMAND_TYPE,
        });
        self
    }

    pub fn register_query<Q: Query>(&mut self) -> &mut Self {
        self.queries.push(QueryInfo {
            query_type: Q::QUERY_TYPE,
            is_list: false,
        });
        self
    }

    pub fn register_list_query<Q: ListQuery>(&mut self) -> &mut Self {
        self.queries.push(QueryInfo {
            query_type: Q::QUERY_TYPE,
            is_list: true,
        });
        self
    }

    pub fn build(self) -> DomainTypes {
        DomainTypes {
            events: self.events,
            tags: self.tags,
            tag_projectors: self.tag_projectors,
            multi_projectors: self.multi_projectors,
            commands: self.commands,
            queries: self.queries,
        }
    }
}

impl ProjectorRegistrar for DomainTypesBuilder {
    fn register_tag_projector<P: TagProjector>(&mut self) {
        self.register_tag_projector::<P>();
    }

    fn register_multi_projector<P: MultiProjector + MultiProjectorQuery>(&mut self) {
        self.register_multi_projector::<P>();
    }
}

impl Default for DomainTypesBuilder {
    fn default() -> Self {
        Self::new()
    }
}

/// Domain definition trait.
pub trait DomainDefinition: Send + Sync + 'static {
    fn register(builder: &mut DomainTypesBuilder);

    fn domain_types() -> DomainTypes {
        let mut builder = DomainTypesBuilder::new();
        Self::register(&mut builder);
        builder.build()
    }
}

impl DomainTypes {
    pub fn deserialize_event(
        &self,
        event_type: &str,
        json: &str,
    ) -> Option<Box<dyn std::any::Any + Send + Sync>> {
        self.events
            .iter()
            .find(|e| e.event_type == event_type)
            .and_then(|e| (e.deserializer)(json))
    }

    pub fn has_event_type(&self, event_type: &str) -> bool {
        self.events.iter().any(|e| e.event_type == event_type)
    }

    pub fn find_tag_projector(&self, name: &str) -> Option<&ProjectorInfo> {
        self.tag_projectors.iter().find(|p| p.name == name)
    }
}

/// Combine multiple domains.
pub struct CombinedDomain<D1, D2> {
    _phantom: std::marker::PhantomData<(D1, D2)>,
}

impl<D1: DomainDefinition, D2: DomainDefinition> DomainDefinition for CombinedDomain<D1, D2> {
    fn register(builder: &mut DomainTypesBuilder) {
        D1::register(builder);
        D2::register(builder);
    }
}
